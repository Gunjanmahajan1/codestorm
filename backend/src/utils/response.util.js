/* -------------------- SUCCESS RESPONSE -------------------- */
exports.successResponse = (
  res,
  data = null,
  message = "Success",
  statusCode = 200
) => {
  return res.status(statusCode).json({
    success: true,
    message,
    data,
  });
};

/* -------------------- ERROR RESPONSE -------------------- */
exports.errorResponse = (
  res,
  message = "Error",
  statusCode = 500
) => {
  return res.status(statusCode).json({
    success: false,
    message,
  });
};
